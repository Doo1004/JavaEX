package com.java.basic;

public class VarEx {
	public static void main(String args[]) {
		//변수 식별자
		//문자, 숫자, $, _의 조합
		//다만 숫자로 시작하면 안됨
		
		/*
		 * 변수명 관련
		 * 변수명은 소문자로 작성
		 * 단어의 조합일 경우 두 번째 단어는 대문자로 시작
		 */
		int myAge;		//myAge라는 이름의 int(4byte) 변수 공간을 만든다
						//초기화
		myAge = 24;
	}
}
